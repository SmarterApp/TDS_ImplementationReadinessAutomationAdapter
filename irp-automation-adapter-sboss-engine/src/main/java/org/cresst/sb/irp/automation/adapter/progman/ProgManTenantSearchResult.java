package org.cresst.sb.irp.automation.adapter.progman;

public class ProgManTenantSearchResult {
    private String id;
    private String name;
    private String type;

    public ProgManTenantSearchResult() {}

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getType() {
        return type;
    }

    public void setType(String type) {
        this.type = type;
    }
}
